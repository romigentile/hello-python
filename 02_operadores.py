# Operadores - Es muy bueno en operadores de calculo

print(3+4)
print(3-4)
print(3*4)
print(3/4)
print(3%4)
print(3**4) #exponente
print(10//3) #aproxima la division a un numero entero

print("Hola " + "Mundo")
print("Hola " + str(5)) #str lo transforma en string
print("Hola " * 5) #se multiplica la palabra

my_float = 2.5 * 2
print(my_float)
print("Hola " * int(my_float))

# Operadores Comparativos

print(3 == 4)
print(3 != 4)
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)

print(3>4 == 2)

